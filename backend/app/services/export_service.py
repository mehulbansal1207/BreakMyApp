"""
export_service.py — Generate Markdown and PDF security reports from scan data.

Supports two output formats:
  - generate_markdown(scan) → str
  - generate_pdf(scan)      → bytes
"""

from __future__ import annotations

import io
import logging
from datetime import datetime, timezone
from typing import Any

from fpdf import FPDF

logger = logging.getLogger(__name__)

_FOOTER_TEXT = "Generated by BreakMyApp • breakmyapp-production-2f29.up.railway.app"

# ---------------------------------------------------------------------------
# Severity colour helpers (used by both renderers)
# ---------------------------------------------------------------------------

_SEVERITY_COLORS: dict[str, tuple[int, int, int]] = {
    "CRITICAL": (220, 38, 38),
    "HIGH": (234, 88, 12),
    "MEDIUM": (202, 138, 4),
    "LOW": (22, 163, 74),
    "INFO": (100, 100, 100),
}


def _severity_color(severity: str) -> tuple[int, int, int]:
    return _SEVERITY_COLORS.get((severity or "").upper(), (100, 100, 100))


def _score_color(score: int | None) -> tuple[int, int, int]:
    if score is None:
        return (100, 100, 100)
    if score >= 80:
        return (22, 163, 74)   # green
    if score >= 60:
        return (202, 138, 4)   # amber
    return (220, 38, 38)       # red


def _trunc(text: str, max_len: int) -> str:
    """Truncate *text* to *max_len* characters, appending '...' if needed."""
    if not text:
        return ""
    s = str(text)
    return s if len(s) <= max_len else s[: max_len - 3] + "..."


